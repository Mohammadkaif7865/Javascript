// Primitive data types
let name = "monu"; //String
console.log("My name is " + name);
console.log("My data type is " + typeof name);
let marks = 34; //Numbers
console.log("My data type is " + typeof marks);
let isDrive = true; //Boolean
console.log("My data type is " + typeof isDrive);
let nullVar = null; //Null
console.log("My data type is " + typeof nullVar);
let undef = undefined; //Undefined
console.log("My data type is " + typeof undef);
// Reference data types
//array
let my_arr = [1, 2, 3, 4, false, null];
console.log("My data type is " + typeof my_arr);
// objects literals
let st_marks = {
    monu: 34,
    sonu: 36,
    mohan: 49,
}
console.log("My data type is " + typeof st_marks);
