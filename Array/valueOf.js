// Definition and Usage
// The valueOf() method returns the array itself.

// The valueOf() method does not change the original array.

// array.valueOf() returns the same as array.
// Return Value
// Type	Description
// An array	The valueOf() method returns itself!
// array.valueOf()
let x = [34, "Monday", "Toss", 34, 534];
let y = x.valueOf(); //return itself or x
console.log(x, y);